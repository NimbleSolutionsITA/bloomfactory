                            <p>Le prime coltivazioni di canapa risalgono a 6000 anni fa in Cina. La canapa è una pianta che contiene più di 400 composti naturali, tra cui troviamo i due principali gruppi di molecole con proprietà attive: i terpeni ed i cannabinoidi. Il CBD appartiene a questi ultimi composti.</p>
                            <p>I cannabinoidi sono molecole capaci di interagire con le funzioni del nostro sistema nervoso. La pianta di canapa biosintetizza più di 60 cannabinoidi che differiscono l'uno dall'altro nei processi d'interazione con il nostro organismo e nella loro specifica struttura molecolare. Tra i più conosciuti troviamo THC, CBD e CBG.</p>
                            <p>Il cannabidiolo, o CBD, è uno dei cannabinoidi più abbondanti nella canapa. È stato ampiamente dimostrato che questa molecola ha diverse proprietà terapeutiche, tra cui la capacità di rilassare il corpo. Sebbene agisca sul nostro organismo offrendo importanti benefici, il CBD non ha proprietà psicoattive come il suo parente più stretto, il THC. Non bisogna quindi confondere l'azione di questi due cannabinoidi.</p>

