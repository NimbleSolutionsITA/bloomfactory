                            <div class="title-tab"><span>Il CBD può sballare?</span></div>
                            <p>Nessun effetto psicoattivo, l'olio di CBD non influisce minimamente su umore, coscienza o facoltà mentali.</p>
                            <p>Potreste chiedervi perché il THC sia in grado di farvi “stonare”, mentre il CBD no. La risposta si trova nel modo in cui questi due cannabinoidi interagiscono con i recettori CB1, concentrati principalmente nel cervello e nel sistema nervoso centrale. I CB1, insieme ai CB2, sono recettori endogeni all'interno del sistema endocannabinoide (ECS). Per farla semplice, il THC si lega ai recettori CB1 come una chiave nella sua serratura, mentre il CBD non presenta la stessa caratteristica.</p>
                            <p>Le molecole di THC sono conformate in maniera idonea per legarsi ai recettori CB1, ed una volta che vi sono connessi, li stimolano immediatamente. In particolare, il THC imita parzialmente l'anandamide, comunemente nota come “molecola della beatitudine”. L'anandamide è un neurotrasmettitore, ed un endocannabinoide, prodotto in maniera naturale dal cervello. L'anandamide è nota per avere un'influenza su tutto, da appetito e memoria, all'inibizione della proliferazione delle cellule cancerose del seno.</p>
                            <p>Il CBD, al contrario, è considerato un “antagonista” dei recettori CB1. Agisce cioè prevalentemente sopprimendo le qualità del THC che attivano tali recettori, il che in ultima istanza minimizza i suoi effetti psicoattivi. Mentre occorrono ulteriori ricerche per determinare esattamente in che modo funzioni questo meccanismo, il CBD viene proposto come modulatore allosterico negativo  del CB1. In sostanza, il CBD limita il “tetto” di psicoattività del THC, ed è per questo che le varietà che contengono sia CBD che THC vengono comunemente ritenute offrire all'utilizzatore medio il meglio di entrambi.</p>
                            <p><b>LO “SBALLO” DEL CBD?</b></p>
                            <img src="/images/mind-weed.jpg" style="float: right; margin: 0 0 25px 25px;">
                            <p>Il CBD può non avere caratteristiche psicoattive in sé e per sé, eppure ha un effetto sul cervello che è altrettanto benefico dello “sballo” indotto dal THC. Una ricerca mostra che, mentre il THC si lega soprattutto con i recettori CB1, il CBD è più connesso con i recettori CB2, che si trovano negli organi periferici e nella pelle, e particolarmente nelle cellule collegate col sistema immunitario. Questo a sua volta può irradiare sugli umani un effetto calmante e sedativo, che si ritiene riduca disturbi fisici e mentali come infiammazione ed ansia.</p>
                            <p>Il CBD vanta effetti ansiolitici, tramite l'attivazione dei recettori dell'adenosina, responsabile essenzialmente di regolare le funzioni cardiovascolari, il flusso sanguigno coronarico, ed il consumo di ossigeno del miocardio. Questi recettori dell'adenosina regolano anche i livelli di dopamina e glutammato nel cervello. Perciò, se pure il CBD non può farvi sballare, esercita comunque benefici psicologici come risultato del suo meccanismo d'azione.</p>
                            <p>In maniera specifica, il CBD ha le potenzialità per aiutare coloro che soffrono di disturbi come PTSD, schizofrenia, epilessia, artrite reumatoide, e nausea, tanto per citarne alcuni. Il futuro suggerisce anche che varietà e prodotti ad alto CBD potrebbero rimpiazzare i farmaci attuali nel trattamento di spasmi muscolari e dolore neuropatico.</p>
