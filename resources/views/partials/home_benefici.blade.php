                            <p>Il sistema endocannabinoide (SEC) è il nome che definisce una serie di specifici recettori che rispondono ad un certo tipo di agonisti. Il SEC è composto da due ricettori cellulari primari, quello dei Cannabinoidi 1 (CB1) e quello dei Canabinoidi 2 (CB2). Quando gli endocannabinoidi, o cannabinoidi, entrano in contatto con questi ricettori producono determinati effetti all'interno del nostro corpo.</p>
